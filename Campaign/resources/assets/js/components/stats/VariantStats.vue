<template>
    <div class="variant-stats well">
        <div class="row">
            <div class="col-sm-12 col-md-8 variant-chart-wrap">
                <chart
                    :name="'variant-stats-chart-' + variant.id"
                    :title="'Variant: ' + variantName + ' <small>(' + variant.proportion + '%)</small>'"
                    :height="400"
                    :loading="loading"
                    :error="error"
                    :chartData="histogramData"
                ></chart>
            </div>
            <div class="col-sm-12 col-md-4">
                <div class="row">
                    <div id="variant-singles-grid" class="clearfix" data-columns>

                        <single-value
                            :title="'CTR'"
                            :unit="'%'"
                            :loading="loading"
                            :error="error"
                            :value="ctr"
                            :infoText="'Click-through rate'"
                        ></single-value>

                        <single-value
                            :title="'Conversions'"
                            :unit="'%'"
                            :loading="loading"
                            :error="error"
                            :value="conversions"
                            :precision="4"
                            :infoText="'Number of purchases / shows.'"
                        ></single-value>

                        <single-value
                            :title="'Shows'"
                            :loading="loading"
                            :error="error"
                            :value="showsCount"
                        ></single-value>

                        <single-value
                            :title="'Clicks'"
                            :loading="loading"
                            :error="error"
                            :value="clickCount"
                        ></single-value>

                        <single-value
                            :title="'Started payments'"
                            :loading="loading"
                            :error="error"
                            :value="startedPaymentsCount"
                        ></single-value>

                        <single-value
                            :title="'Purchases'"
                            :loading="loading"
                            :error="error"
                            :value="finishedPaymentsCount"
                        ></single-value>

                        <single-value
                            :title="'Earned'"
                            :loading="loading"
                            :error="error"
                            :value="earnedSum"
                            :unit="'â‚¬'"
                        ></single-value>

                    </div>
                </div>
            </div>
        </div>
    </div>
</template>

<script>
    import SingleValue from './SingleValue'
    import Chart from './Chart'

    export default {
        props: {
            variant: {
                type: Object,
                required: true
            },
            data: {
                type: Object,
                required: true,
                default() {
                    return {};
                }
            },
            loading: {
                type: Boolean,
                required: true
            },
            error: {
                type: String,
                required: true,
                default: ""
            }
        },
        components: {
            SingleValue,
            Chart
        },
        data() {
            return {
                clickCount: 0,
                showsCount: 0,
                startedPaymentsCount: 0,
                finishedPaymentsCount: 0,
                earnedSum: 0,
                ctr: 0,
                conversions: 0,
                histogramData: {},
            }
        },
        computed: {
            variantName() {
                if (this.variant.banner !== null) {
                    return this.variant.banner.name;
                }

                return 'Control Group'
            }
        },
        watch: {
            data(data) {
                this.clickCount = data.click_count.count;
                this.showsCount = data.show_count.count;
                this.startedPaymentsCount = data.payment_count.count;
                this.finishedPaymentsCount = data.purchase_count.count;
                this.earnedSum = data.purchase_sum.sum;
                this.histogramData = data.histogram;
                this.ctr = data.ctr;
                this.conversions = data.conversions;
            }
        }
    }
</script>
